import type { DataPortSchemaObject, FromSchema, TypedArraySchemaOptions } from "@workglow/util";
import type {
  AutoGeneratedKeys,
  DeleteSearchCriteria,
  InsertEntity,
  ITabularStorage,
  QueryOptions,
  SearchCriteria,
  SimplifyPrimaryKey,
  TabularChangePayload,
  TabularEventListener,
  TabularEventName,
  TabularEventParameters,
  TabularSubscribeOptions,
} from "./ITabularStorage";
import { traced } from "../util/traced";

/**
 * Telemetry wrapper for any ITabularStorage implementation.
 * Creates spans for all storage operations.
 */
export class TelemetryTabularStorage<
  Schema extends DataPortSchemaObject,
  PrimaryKeyNames extends ReadonlyArray<keyof Schema["properties"]>,
  Entity = FromSchema<Schema, TypedArraySchemaOptions>,
  PrimaryKey = SimplifyPrimaryKey<Entity, PrimaryKeyNames>,
  InsertType = InsertEntity<Entity, AutoGeneratedKeys<Schema>>,
> implements ITabularStorage<Schema, PrimaryKeyNames, Entity, PrimaryKey, InsertType> {
  constructor(
    protected readonly storageName: string,
    protected readonly inner: ITabularStorage<
      Schema,
      PrimaryKeyNames,
      Entity,
      PrimaryKey,
      InsertType
    >
  ) {}

  put(value: InsertType): Promise<Entity> {
    return traced("workglow.storage.tabular.put", this.storageName, () => this.inner.put(value));
  }

  putBulk(values: InsertType[]): Promise<Entity[]> {
    return traced("workglow.storage.tabular.putBulk", this.storageName, () =>
      this.inner.putBulk(values)
    );
  }

  get(key: PrimaryKey): Promise<Entity | undefined> {
    return traced("workglow.storage.tabular.get", this.storageName, () => this.inner.get(key));
  }

  delete(key: PrimaryKey | Entity): Promise<void> {
    return traced("workglow.storage.tabular.delete", this.storageName, () =>
      this.inner.delete(key)
    );
  }

  getAll(options?: QueryOptions<Entity>): Promise<Entity[] | undefined> {
    return traced("workglow.storage.tabular.getAll", this.storageName, () =>
      this.inner.getAll(options)
    );
  }

  deleteAll(): Promise<void> {
    return traced("workglow.storage.tabular.deleteAll", this.storageName, () =>
      this.inner.deleteAll()
    );
  }

  size(): Promise<number> {
    return traced("workglow.storage.tabular.size", this.storageName, () => this.inner.size());
  }

  deleteSearch(criteria: DeleteSearchCriteria<Entity>): Promise<void> {
    return traced("workglow.storage.tabular.deleteSearch", this.storageName, () =>
      this.inner.deleteSearch(criteria)
    );
  }

  getBulk(offset: number, limit: number): Promise<Entity[] | undefined> {
    return traced("workglow.storage.tabular.getBulk", this.storageName, () =>
      this.inner.getBulk(offset, limit)
    );
  }

  query(
    criteria: SearchCriteria<Entity>,
    options?: QueryOptions<Entity>
  ): Promise<Entity[] | undefined> {
    return traced("workglow.storage.tabular.query", this.storageName, () =>
      this.inner.query(criteria, options)
    );
  }

  // Forwarded directly (async generators, not worth tracing)
  records(pageSize?: number): AsyncGenerator<Entity, void, undefined> {
    return this.inner.records(pageSize);
  }

  pages(pageSize?: number): AsyncGenerator<Entity[], void, undefined> {
    return this.inner.pages(pageSize);
  }

  subscribeToChanges(
    callback: (change: TabularChangePayload<Entity>) => void,
    options?: TabularSubscribeOptions
  ): () => void {
    return this.inner.subscribeToChanges(callback, options);
  }

  setupDatabase(): Promise<void> {
    return this.inner.setupDatabase();
  }

  destroy(): void {
    return this.inner.destroy();
  }

  [Symbol.dispose](): void {
    return this.inner[Symbol.dispose]();
  }

  [Symbol.asyncDispose](): Promise<void> {
    return this.inner[Symbol.asyncDispose]();
  }

  // Event delegation — no telemetry needed
  on<Event extends TabularEventName>(
    name: Event,
    fn: TabularEventListener<Event, PrimaryKey, Entity>
  ): void {
    this.inner.on(name, fn);
  }

  off<Event extends TabularEventName>(
    name: Event,
    fn: TabularEventListener<Event, PrimaryKey, Entity>
  ): void {
    this.inner.off(name, fn);
  }

  emit<Event extends TabularEventName>(
    name: Event,
    ...args: TabularEventParameters<Event, PrimaryKey, Entity>
  ): void {
    this.inner.emit(name, ...args);
  }

  once<Event extends TabularEventName>(
    name: Event,
    fn: TabularEventListener<Event, PrimaryKey, Entity>
  ): void {
    this.inner.once(name, fn);
  }

  waitOn<Event extends TabularEventName>(
    name: Event
  ): Promise<TabularEventParameters<Event, PrimaryKey, Entity>> {
    return this.inner.waitOn(name);
  }
}
